#include "3-calc.h"

/**
 * op_add - sum two numbers
 * @a: integer value 1
 * @b: integer value 2
 *
 * Return: integer Value
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: integer value 1
 * @b: integer value 2
 *
 * Return: integer Value
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two numbers
 * @a: integer value 1
 * @b: integer value 2
 *
 * Return: integer Value
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers
 * @a: integer value 1
 * @b: integer value 2
 *
 * Return: integer Value
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - extract remainderof two numbers
 * @a: integer value 1
 * @b: integer value 2
 *
 * Return: integer Value
 */
int op_mod(int a, int b)
{
	return (a % b);
}
