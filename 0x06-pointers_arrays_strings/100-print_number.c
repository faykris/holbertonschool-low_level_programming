#include "holberton.h"
/**
 * print_number - function that prints an integer
 * @n: integer variable
 * Return: nothing
 */
void print_number(int n)
{
	int INT_MIN = -2147483648, INT_MAX = 2147483647, neg = 0;

if (n < 0)
{	putchar(45);
	if (n == INT_MIN)
	{	neg = 1;
		n++;
	}
	n = -(n);
}
if (n > 999999999 && n < 10000000000)
{	putchar('0' + n / 1000000000);
	putchar('0' + (n % 1000000000) / 100000000);
	putchar('0' + ((n % 1000000000) % 100000000) / 10000000);
	putchar('0' + (((n % 1000000000) % 100000000) % 10000000) / 1000000);
putchar('0' + ((((n % 1000000000)
		 % 100000000) % 10000000) % 1000000) / 100000);
putchar('0' + (((((n % 1000000000) % 100000000)
		 % 10000000) % 1000000) % 100000) / 10000);
putchar('0' + ((((((n % 1000000000) % 100000000) % 10000000)
		 % 1000000) % 100000) % 10000) / 1000);
putchar('0' + (((((((n % 1000000000) % 100000000) % 10000000)
		  % 1000000) % 100000) % 10000) % 1000) / 100);
putchar('0' + ((((((((n % 1000000000) % 100000000) % 10000000)
		   % 1000000) % 100000) % 10000) % 1000) % 100) / 10);
} else if (n > 999 && n < 10000)
{	putchar('0' + n / 1000);
	putchar('0' + (n % 1000) / 100);
	putchar('0' + ((n % 1000) % 100) / 10);
} else if (n > 99 && n < 1000)
{	putchar('0' + n / 100);
	putchar('0' + (n % 100) / 10);
} else if (n > 9 && n < 999)
	putchar('0' + n / 10);
if (-(n) - 1 == INT_MIN && neg == 1)
	putchar('0' + (n % 10) + 1);
else
	putchar('0' + n % 10);
}
